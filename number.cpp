//Identify even odd number
#include <iostream>
using namespace std;
int main()
{
    int number;
    cout<<"Enter the Number:";
    cin>>number;
    if(number%2==0)
    {
        cout<<"Number is Even";
    }
    else
    {
        cout<<"Number is odd";
    }
    return 0;
}
//Enter the Number:55
//Number is odd